package ProgramingClass;

public class CharacterStringType {
    public static void main(String[] args) {

        char bar = 'a';  // ' '  -> Char  (유니 코드 등 글자를 정수로 저장)
        String foo = "12";  // " "  -> String (2글자 이상의 문장을 저장)
        int pos = 30;

        System.out.println(foo + pos);  // 1230

    }
}
